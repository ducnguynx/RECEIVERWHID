/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */
  HAL_Delay(200);
  nrf24_Init();
  nrf24_RxMode(RxAddress, 10);
  HAL_Delay(200);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	if (isDataAvailable(1)==1) {
	nrf24_Receive(RxData);


	  		Accel_X_RAW = (int16_t)(RxData[0] << 8 | RxData [1]);
	  		Accel_Y_RAW = (int16_t)(RxData[2] << 8 | RxData [3]);
	  		Accel_Z_RAW = (int16_t)(RxData[4] << 8 | RxData [5]);

	  		newx = Accel_X_RAW/256;
	  		newy = Accel_Y_RAW/256;
	  		newz = Accel_Z_RAW/256; // squash the value

	  		x = newy;
	  		y = -newx;

	  		if (x >10) {
				mouseid.mouse_x = (x-8)/2;
			}
	  		else if (x<-10) {mouseid.mouse_x = (x+8)/2;}
	  		else mouseid.mouse_x = 0;
	  		if (y >10) {
	  		mouseid.mouse_y = (y-8)/2;
	  		}
	  		else if (y<-10) {mouseid.mouse_y = (y+8)/2;}
	  		else mouseid.mouse_y = 0;
	  		if (RxData [12] == 1)
	  		{
	  		mouseid.button = 1;
	  		mouseid.mouse_x = 0;
	  		mouseid.mouse_y = 0;
	  		USBD_HID_SendReport(&hUsbDeviceFS, &mouseid, sizeof (mouseid));
	  		HAL_Delay (50);
	  		mouseid.button = 0;
	  		USBD_HID_SendReport(&hUsbDeviceFS,&mouseid, sizeof (mouseid));
	  		}
	  		USBD_HID_SendReport(&hUsbDeviceFS, &mouseid, sizeof(mouseid));

	 }
  }
  /* USER CODE END 3 */
}

